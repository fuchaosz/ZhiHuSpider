class ProxyTool():

    def __init__(self):
        self.headers = headers
        self.base_proxy_url = base_proxy_url
        self.detect_proxy_url = detect_proxy_url
        self.session = requests.session()
        self.session.keep_alive = False
        self.page = 1

    #获取指定页面上的代理IP和端口
    def getProxyIpByPage(self,page):
        url = base_proxy_url.format(page)
        req = requests.get(url,headers=headers)
        p = pq(req.text)
        allitems = p('tr.odd')
        list = []
        for item in allitems.items():
            str = item.text().split()
            #存活时间以天为单位的不抓
            if str[5][-1] == '天':
                continue
            #代理类型必须是http或者https，去掉socket类型代理
            if str[4] == 'HTTP' or str[4] == 'HTTPS':
                list.append(str)
        return list

    #检测抓到的IP是否有效
    def detectProxyIP(self,ip,port):
        result = False
        proxies = {
            'http':'http://{0}:{1}'.format(ip,port),
            'https':'http://{0}:{1}'.format(ip,port)
        }
        try:
            r = self.session.get(self.detect_proxy_url,proxies=proxies,headers=self.headers)
            if r.status_code == 200:
                result = True
                print(r.text)
        except Exception as e:
            result = False
            print(e)
        return  result

    #每次获取一页的IP并检测有效的ip，超过20页后重新开始
    def getProxyIPOnce(self):
        if self.page > 20:
            self.page = 1
        list = self.getProxyIpByPage(self.page)
        for item in list:
            if self.detectProxyIP(item[0], item[1]):
                print('发现一个有效代理:', item)
            else:
                list.remove(item)
        return list

    #从西刺提供的API文件中读取ip
    def getProxyIpByFile(self):
        url = r'http://api.xicidaili.com/free2016.txt'
        list = []
        r = requests.get(url,headers=headers)
        str = r.text.split('\r\n')
        for item in str:
            ip = item.split(':')[0]
            port = item.split(':')[1]
            list.append((ip,port))
        return list

